package com.tetris.model;

import java.awt.Color;

// 座標系は(列x, 行y)、原点はバウンディングボックス左上、yは下方向が正
public enum TetrominoType {

    I(new int[][][]{
            {{0, 1}, {1, 1}, {2, 1}, {3, 1}},
            {{2, 0}, {2, 1}, {2, 2}, {2, 3}},
            {{0, 2}, {1, 2}, {2, 2}, {3, 2}},
            {{1, 0}, {1, 1}, {1, 2}, {1, 3}}
    }, new Color(0, 240, 240)),

    J(new int[][][]{
            {{0, 0}, {0, 1}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {1, 2}},
            {{0, 1}, {1, 1}, {2, 1}, {2, 2}},
            {{1, 0}, {1, 1}, {0, 2}, {1, 2}}
    }, new Color(0, 0, 240)),

    L(new int[][][]{
            {{2, 0}, {0, 1}, {1, 1}, {2, 1}},
            {{1, 0}, {1, 1}, {1, 2}, {2, 2}},
            {{0, 1}, {1, 1}, {2, 1}, {0, 2}},
            {{0, 0}, {1, 0}, {1, 1}, {1, 2}}
    }, new Color(240, 160, 0)),

    O(new int[][][]{
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}}
    }, new Color(240, 240, 0)),

    S(new int[][][]{
            {{1, 0}, {2, 0}, {0, 1}, {1, 1}},
            {{1, 0}, {1, 1}, {2, 1}, {2, 2}},
            {{1, 1}, {2, 1}, {0, 2}, {1, 2}},
            {{0, 0}, {0, 1}, {1, 1}, {1, 2}}
    }, new Color(0, 240, 0)),

    T(new int[][][]{
            {{1, 0}, {0, 1}, {1, 1}, {2, 1}},
            {{1, 0}, {1, 1}, {2, 1}, {1, 2}},
            {{0, 1}, {1, 1}, {2, 1}, {1, 2}},
            {{1, 0}, {0, 1}, {1, 1}, {1, 2}}
    }, new Color(160, 0, 240)),

    Z(new int[][][]{
            {{0, 0}, {1, 0}, {1, 1}, {2, 1}},
            {{2, 0}, {1, 1}, {2, 1}, {1, 2}},
            {{0, 1}, {1, 1}, {1, 2}, {2, 2}},
            {{1, 0}, {0, 1}, {1, 1}, {0, 2}}
    }, new Color(240, 0, 0));

    private final int[][][] rotationShapes;
    private final Color color;

    TetrominoType(int[][][] rotationShapes, Color color) {
        this.rotationShapes = rotationShapes;
        this.color = color;
    }

    public int[][] getShape(int rotationState) {
        return rotationShapes[rotationState];
    }

    public Color getColor() {
        return color;
    }
}
