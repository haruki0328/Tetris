package com.tetris.model;

import java.awt.Color;

/**
 * 7種類のテトリミノ（I, J, L, O, S, T, Z）の種別を表す列挙型。
 *
 * <p>各テトリミノは、Tetris Guideline（SRS: Super Rotation System）に準拠した
 * 4x4のバウンディングボックス上での4方向（0, R, 2, L）の形状データを保持する。</p>
 *
 * <p>座標系は (列x, 行y) の順で、原点はバウンディングボックスの左上。
 * yはボード描画と同様に下方向が正である。</p>
 */
public enum TetrominoType {

    /** I型テトリミノ（水色）。回転すると横一列⇔縦一列に切り替わる。 */
    I(new int[][][]{
            {{0, 1}, {1, 1}, {2, 1}, {3, 1}},
            {{2, 0}, {2, 1}, {2, 2}, {2, 3}},
            {{0, 2}, {1, 2}, {2, 2}, {3, 2}},
            {{1, 0}, {1, 1}, {1, 2}, {1, 3}}
    }, new Color(0, 240, 240)),

    /** J型テトリミノ（青）。 */
    J(new int[][][]{
            {{0, 0}, {0, 1}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {1, 2}},
            {{0, 1}, {1, 1}, {2, 1}, {2, 2}},
            {{1, 0}, {1, 1}, {0, 2}, {1, 2}}
    }, new Color(0, 0, 240)),

    /** L型テトリミノ（オレンジ）。 */
    L(new int[][][]{
            {{2, 0}, {0, 1}, {1, 1}, {2, 1}},
            {{1, 0}, {1, 1}, {1, 2}, {2, 2}},
            {{0, 1}, {1, 1}, {2, 1}, {0, 2}},
            {{0, 0}, {1, 0}, {1, 1}, {1, 2}}
    }, new Color(240, 160, 0)),

    /** O型テトリミノ（黄色）。回転しても形状は変化しない。 */
    O(new int[][][]{
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}},
            {{1, 0}, {2, 0}, {1, 1}, {2, 1}}
    }, new Color(240, 240, 0)),

    /** S型テトリミノ（緑）。 */
    S(new int[][][]{
            {{1, 0}, {2, 0}, {0, 1}, {1, 1}},
            {{1, 0}, {1, 1}, {2, 1}, {2, 2}},
            {{1, 1}, {2, 1}, {0, 2}, {1, 2}},
            {{0, 0}, {0, 1}, {1, 1}, {1, 2}}
    }, new Color(0, 240, 0)),

    /** T型テトリミノ（紫）。 */
    T(new int[][][]{
            {{1, 0}, {0, 1}, {1, 1}, {2, 1}},
            {{1, 0}, {1, 1}, {2, 1}, {1, 2}},
            {{0, 1}, {1, 1}, {2, 1}, {1, 2}},
            {{1, 0}, {0, 1}, {1, 1}, {1, 2}}
    }, new Color(160, 0, 240)),

    /** Z型テトリミノ（赤）。 */
    Z(new int[][][]{
            {{0, 0}, {1, 0}, {1, 1}, {2, 1}},
            {{2, 0}, {1, 1}, {2, 1}, {1, 2}},
            {{0, 1}, {1, 1}, {1, 2}, {2, 2}},
            {{1, 0}, {0, 1}, {1, 1}, {0, 2}}
    }, new Color(240, 0, 0));

    /**
     * 回転状態ごとのブロック座標データ。
     * <p>次元は [回転状態(0-3)][ブロック番号(0-3)][x/y(0-1)]。</p>
     */
    private final int[][][] rotationShapes;

    /** この種別の描画色。 */
    private final Color color;

    /**
     * テトリミノ種別を初期化する。
     *
     * @param rotationShapes 回転状態ごとの形状座標データ
     * @param color          描画色
     */
    TetrominoType(int[][][] rotationShapes, Color color) {
        this.rotationShapes = rotationShapes;
        this.color = color;
    }

    /**
     * 指定した回転状態におけるブロック座標（バウンディングボックス内の相対座標）を取得する。
     *
     * @param rotationState 回転状態（0〜3）
     * @return 4ブロック分の相対座標配列（各要素は {x, y}）
     */
    public int[][] getShape(int rotationState) {
        return rotationShapes[rotationState];
    }

    /**
     * このテトリミノ種別の描画色を取得する。
     *
     * @return 描画色
     */
    public Color getColor() {
        return color;
    }
}
